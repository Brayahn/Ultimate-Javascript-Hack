/**
 Simple Strings 
 */
const single = "Single quotes";
const double = "Double quotes";

/**
 Complex strings
 */
const backtick = `This is not a ${single} or a ${double} quotes example`;

console.log(backtick);

/**
 Escape characters in strings
 */
const third = 'People call me Brian, or "Brayo" ';
console.log(third);

/**
 Rows in Strings, Simple strings don't support this. 
 */

const numbers = `1
2
3
4`;

console.log(numbers);
